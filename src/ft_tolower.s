section .text
	global _ft_tolower

;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;
;;; CORE 
;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;

_ft_tolower:
	mov		rax, rdi

comp:
	cmp		rdi, 0x41
	jl		end

	cmp		rdi, 0x5a
	jle		lower

	jmp		end

lower:
	add		rdi, 32
	mov		rax, rdi

	jmp		end

end:
	ret