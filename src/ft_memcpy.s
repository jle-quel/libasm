section	.text
	global _ft_memcpy

;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;
;;; PUBLIC FUNCTION
;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;

_ft_memcpy:
	push	rbp
	mov		rbp, rsp

	cmp		rdi, 0x0
	je		.end

	cmp		rsi, 0x0
	je		.end

.init:
	mov		rcx, rdx
	mov		rax, rdi
	rep		movsb

.end:
	leave
	ret