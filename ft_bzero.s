;;; TEXT	
;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;

section	.text
	global _ft_bzero

;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;
;;; CORE	
;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;

_ft_bzero:
	push rbp
	mov rbp, rsp

	mov rax, rdi
	mov rcx, 0

	cmp rdi, 0x0
	je end

iter:
	cmp rsi, rcx
	je end

	mov byte[rdi], 0

	inc rdi
	inc rcx
	jmp iter 

end:
	mov rdi, rax

	leave
	ret